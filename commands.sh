#COMMANDS

# ls === list (all the items inside a dir)
# cd === change directory
# cd ~ === back to root dir
# cd .. === one level back
# clear === cleans cmd
# mkdir === create folder
# touch === create file
# start === open file
# code === opens file in vsCode
# rm === delete file
# pwd === prints current dir location
# rm * === removes all files in a dir
# rm -r === remove dir

#SHORTCUTS 
# arrow up, down === cycles past commands
# alt R,L === gets to end of a word